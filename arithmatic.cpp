//arithmetics operations

#include<iostream>
using namespace std;

int main() {
    int a=10, b=15;

    cout<<"sum="<<(a+b)<<endl;
    cout<<"difference="<<(a-b)<<endl;
    cout<<"multiplication="<<(a*b)<<endl;
    cout<<"division="<<(a/b)<<endl;
    cout<<"module="<<(a%b)<<endl;
    return 0;
}